export const getMovieById = (id) => {
  const filteredMovie = movieList.filter((movie) => id === movie.id);
  return filteredMovie[0];
};

export const deleteMovie = (id) => {
  const cleanedMovieList = movieList.filter((movie) => id !== movie.id);
  if (movieList.length > cleanedMovieList.length) {
    movieList = cleanedMovieList;
    return true;
  }
  return false;
};

export const addMovie = (title, popularity) => {
  const newMovie = {
    id: movieList.length + 1,
    title,
    popularity,
  };
  movieList.push(newMovie);
  return newMovie;
};
